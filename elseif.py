# elif di dalama bahasa pemograman python adalah peryataan yang digunakan untuk menambahakan kondisi lain setelaha if

nilai = int(input("masukan nilai anda: "))
if nilai >= 90:
    print("nilai anda bagus")
elif nilai >= 80:
    print("nilai anda cukup bagus")
elif nilai >= 70:
    print("nilai anda cukup")
elif nilai >= 60:
    print("nilai anda kurang")
else: 
    print("nilai anda tidak lulus")