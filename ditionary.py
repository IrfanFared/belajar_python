# Contoh penggunaan dictionary

isi_kotak = {
    "kotak1" : "bolu",
     "kotak2" : "kue"
}

print(f"contoh pengunaan dictionary ini adalah kotak yang isinya {isi_kotak['kotak1']}") # yup penganti seprti index gampanya